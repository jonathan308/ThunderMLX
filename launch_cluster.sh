#
# launch_cluster.sh — start the 2-node MiniMax-M3 cluster via mlx.launch
#
# Copy .env.example to .env.local and fill in your own hostnames, model path,
# RDMA devices, memory limits, and public endpoint before starting.
#
set -euo pipefail

SCRIPT_DIR="${0:A:h}"
