HOSTFILE="${M3_HOSTFILE:-/private/tmp/m3_mlx_hosts.json}"
BACKEND="${M3_MLX_BACKEND:-jaccl}"
LAUNCH_HOST_ARGS=()
RANK1_DIRECT_SSH="${M3_RANK1_DIRECT_SSH:-worker.local}"
RANK1_FALLBACK_SSH="${M3_RANK1_FALLBACK_SSH:-${M3_RANK1_TAILSCALE_SSH:-}}"
RANK1_SSH="$RANK1_DIRECT_SSH"
RANK0_DATA_IP="${M3_RANK0_DATA_IP:-primary.local}"
RANK1_DATA_IP="${M3_RANK1_DATA_IP:-worker.local}"
RANK0_RDMA="${M3_RANK0_RDMA:-}"
RANK1_RDMA="${M3_RANK1_RDMA:-}"
RANK0_RDMA_JSON="null"
RANK1_RDMA_JSON="null"
if [[ -n "$RANK0_RDMA" ]]; then RANK0_RDMA_JSON="\"$RANK0_RDMA\""; fi
if [[ -n "$RANK1_RDMA" ]]; then RANK1_RDMA_JSON="\"$RANK1_RDMA\""; fi

case "$BACKEND" in
  jaccl|jaccl-ring)
    if ! ssh -o BatchMode=yes -o ConnectTimeout=5 -o ConnectionAttempts=1 \
        "$RANK1_DIRECT_SSH" 'true' >/dev/null 2>&1; then
      if [[ -n "$RANK1_FALLBACK_SSH" ]]; then
        RANK1_SSH="$RANK1_FALLBACK_SSH"
        echo "[launch] WARN: direct SSH to worker unavailable; using fallback SSH control only." >&2
      else
        echo "[launch] ERROR: rank 1 SSH is unreachable and no M3_RANK1_FALLBACK_SSH is set." >&2
        exit 2
      fi
    fi
    # Hostfile separates SSH control plane from distributed data plane:
    # - rank 0 runs locally (127.0.0.1, no ssh needed)
    # - rank 1 may launch over direct SSH or fallback SSH control
    # - JACCL coordinator/IPs/RDMA devices use the configured data link
    cat > "$HOSTFILE" <<EOF
{
  "backend": "$BACKEND",
  "envs": [],
  "hosts": [
    {"ssh": "127.0.0.1", "ips": ["$RANK0_DATA_IP"], "rdma": [null, $RANK0_RDMA_JSON]},
    {"ssh": "$RANK1_SSH", "ips": ["$RANK1_DATA_IP"], "rdma": [$RANK1_RDMA_JSON, null]}
  ]
}
EOF
    LAUNCH_HOST_ARGS=(--hostfile "$HOSTFILE")
    echo "[launch] using $BACKEND hostfile: SSH=local/rank1, data=$RANK0_DATA_IP<->$RANK1_DATA_IP" >&2
    ;;
  ring)
    if [[ -n "${M3_MLX_HOSTS:-}" ]]; then
      HOSTS="$M3_MLX_HOSTS"
    elif ssh -o BatchMode=yes -o ConnectTimeout=5 -o ConnectionAttempts=1 \
        "$RANK1_DIRECT_SSH" 'true' >/dev/null 2>&1; then
      HOSTS="$DIRECT_HOSTS"
    else
      HOSTS="$FALLBACK_HOSTS"
      echo "[launch] WARN: direct rank 1 SSH unavailable; using fallback hosts for ring backend." >&2
    fi
    LAUNCH_HOST_ARGS=(--backend ring --hosts "$HOSTS")
    ;;
  *)
    echo "[launch] ERROR: unsupported M3_MLX_BACKEND=$BACKEND (use jaccl, jaccl-ring, or ring)" >&2
    exit 2
    ;;
esac

if [[ "${MLX_M3_APPLY_RUNTIME_PATCHES:-1}" == "1" ]]; then
