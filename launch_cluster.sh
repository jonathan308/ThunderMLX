# Production split:
#   Rank 0: final layers, norm, lm_head, and OpenAI API
#   Rank 1: initial layers and embeddings
#
# COMMAND BUFFER SIZING (the GPU-timeout fix):
#   MLX_MAX_OPS_PER_BUFFER / MLX_MAX_MB_PER_BUFFER cap how much work goes into
#   a SINGLE Metal command buffer. If one buffer takes >~10s to execute, the
#   GPU driver kills it (kIOGPUCommandBufferCallbackErrorTimeout -> SIGABRT).
#   This is the REAL root cause that hid behind the watchdog crashes: on the
#   Worker processing a large layer slice can make a large prefill buffer
#   512MB easily exceeded the driver timeout. Smaller buffers (16 ops / 128MB)
#   complete well within the timeout, so async_eval stays enabled (no prefill
#   stall) and no buffer triggers the driver kill. If GPU timeouts return,
#   LOWER memory further (e.g. 2 ops / 16MB) if sustained generation stalls.
